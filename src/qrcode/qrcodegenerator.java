    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {

            String qrname = name.getText();
            ByteArrayOutputStream out = QRCode.from(qrname).to(ImageType.JPG).stream();
            String file = "C:\\Users\\kimde\\Pictures\\QRCodeSample\\" + qrname + ".JPG";
            File f = new File(file);
            FileOutputStream fos = new FileOutputStream(f);
            fos.write(out.toByteArray());
            fos.flush();

            code.setIcon(new ImageIcon(new ImageIcon(file).getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH)));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }


    }//GEN-LAST:event_jButton1ActionPerformed

